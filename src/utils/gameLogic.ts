import { BoardState, CellValue, Player } from '../types/game';

export const WINNING_LINES: number[][] = [
  [0, 1, 2],
  [3, 4, 5],
  [6, 7, 8],
  [0, 3, 6],
  [1, 4, 7],
  [2, 5, 8],
  [0, 4, 8],
  [2, 4, 6]
];

export const createEmptyBoard = (): BoardState => Array<CellValue>(9).fill(null);

export const determineWinner = (
  board: BoardState
): { winner: Player; line: number[] } | null => {
  for (const line of WINNING_LINES) {
    const [a, b, c] = line;
    if (board[a] && board[a] === board[b] && board[a] === board[c]) {
      return { winner: board[a] as Player, line };
    }
  }

  return null;
};

export const isBoardFull = (board: BoardState): boolean => board.every(Boolean);
